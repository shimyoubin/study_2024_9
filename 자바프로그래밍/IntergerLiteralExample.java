package ch02.sce01;

public class IntergerLiteralExample {

	public static void main(String[] args) {
		int v1 = 0b1011;
		int v2 = 0206;
		int v3 = 365;
		int v4 = 0xB3;
		
		System.out.println("var1: " + v1);
		System.out.println("var2: " + v2);
		System.out.println("var3: " + v3);
		System.out.println("var4: " + v4);
	}

}
